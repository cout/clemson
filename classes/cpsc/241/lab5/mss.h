//  Header file (function prototypes) for maximum subsequence sum functions.
//
//  Each of the following functions computes the maximum subsequence sum for
//  a given sequence.  The first parameter, A, of each is an integer array and
//  the second parameter, N, is the number of elements of A that represent the
//  sequence.  Thus the sequence is in A[0..N-1].

int Max_Subsequence_Sum1( const int A[ ], const unsigned int N );

int Max_Subsequence_Sum2( const int A[ ], const unsigned int N );

int Max_Subsequence_Sum3( const int A[ ], const unsigned int N );

int Max_Subsequence_Sum4( const int A[ ], const unsigned int N );
